# jumping-pass,continue,break
for i in range(1,50):
    if i == 25:
        break#to exit a loop
    print(i,end=" ")
print()
#continue
for i in range(1,50):
    if i == 25:
        continue#to skip  a condition
    print(i,end=" ")
print()
#pass
for i in range(1,50):
    if i == 25:
        pass#do nothing
    print(i,end=" ")


